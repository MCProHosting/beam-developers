import React from "react";
import Method from "../../method.js";
import Parameter from "../../parameter.js";
import Example from "../../example.js";

import SuccessfulResponse from "./successfulResponse.js";

export default class deleteOauthAuthorizationMethod extends Method {
  uri() { return "/api/v1/oauth/authorized/:id"; }
  version() { return 1; }
  httpMethod() { return "DELETE"; }
  group() { return "oauth" }
  description() {
    return (
      <p>
        Deletes all authorizations for the provided client. This
        results in all keys generated by the client for that user
        to be invalidated.
      </p>
    )
  }

  parameters() {
    return [
      new IDParameter()
    ];
  }
  examples() {
    return [
      new SuccessfulResponse(),
      new ErrorfulResult()
    ];
  }
}

class IDParameter extends Parameter {
  name() { return "id"; }
  description() { return "The id string for the OAuth application."; }
}

class ErrorfulResult extends Example {
  httpCode() { return 404; }
  data() { return "Client not found."; }
}
